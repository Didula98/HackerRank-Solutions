def squares(a, b):
    # Write your code here
    # squreRoot = pow(a, 0.5)
    lowerBound = math.floor(pow(a, 0.5))
    count = 0
    while pow(lowerBound, 2) <= b:
        if pow(lowerBound, 2) >= a:
            count +=1
        lowerBound +=1
    return count
        

